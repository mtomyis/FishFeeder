package com.tambakudang.tomtom.tambak.grafik;

public class GrafikModel {

    String waktu, suhu, ph, oksigen;

    public String getWaktu() {
        return waktu;
    }

    public void setWaktu(String waktu) {
        this.waktu = waktu;
    }

    public String getSuhu() {
        return suhu;
    }

    public void setSuhu(String suhu) {
        this.suhu = suhu;
    }

    public String getPh() {
        return ph;
    }

    public void setPh(String ph) {
        this.ph = ph;
    }

    public String getOksigen() {
        return oksigen;
    }

    public void setOksigen(String oksigen) {
        this.oksigen = oksigen;
    }
}
